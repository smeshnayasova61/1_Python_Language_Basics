#2. Представлен список чисел. Необходимо вывести элементы исходного списка, значения которых
# больше предыдущего элемента.
# Подсказка: элементы, удовлетворяющие условию, оформить в виде списка. Для его формирования используйте генератор.
# Пример исходного списка: [300, 2, 12, 44, 1, 1, 4, 10, 7, 1, 78, 123, 55]. Результат: [12, 44, 4, 10, 78, 123].
my_list = [300, 2, 12, 44, 1, 1, 4, 10, 7, 1, 78, 123, 55] #переменная в виде списка
more_then = [my_list[num] for num in range(1, len(my_list)) if my_list[num] > my_list[num -1]] # переменная, которую выводим с помощью генератора списка
# в генераторе переменная my_list, объявляем переремнную num, переменную num ищем в цикле в области списка my_list от 1 до последнего элемента в списке по индексам,
# выполняем условие если значение текущего элемента больше предыдущего, записать его в новый список
print(more_then)

