from random import randint#из модуля random возьмем randint

original_list = [randint(0, 1000) for _ in range(0, randint(2, 20))] #берем случайный элемент 0-1000, в цикле ищеи безымянную переменную в области от 0 до случайного элемента от 2-20
answer_list = [num for i, num in enumerate(original_list[1:]) if num > original_list[i]] #enumerate возвращает кортеж где на первой позиции индекс-i, а на второй значение- num, переменный i и num ищем в original_list-берем слайс  1 индекса,
# if мы сравниваем num с полноценным original-list(где есть значение с 0 индексом и берем само значение num(переменная до цикла for) без индексов)
print(original_list)
print(answer_list)